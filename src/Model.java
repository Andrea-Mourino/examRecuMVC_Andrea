public class Model {

    public static double sumarM(double a, double b) {
        return a + b;
    }

    public static double restarM(double a, double b) {
        return a - b;
    }

    public static double multiplicarM(double a, double b) {
        return a * b;
    }

    public static double dividirM(double a, double b) {
        return a / b;
    }
}
