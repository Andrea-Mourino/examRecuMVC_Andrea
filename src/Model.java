public class Model {

    public static double sumarM(double numero1, double numero2) {
        return numero1 + numero2;
    }

    public static double restarM(double numero1, double numero2) {
        return numero1 - numero2;
    }

    public static double multiplicarM(double numero1, double numero2) {
        return numero1 * numero2;
    }

    public static double dividirM(double numero1, double numero2) {
        return numero1 / numero2;
    }
}
